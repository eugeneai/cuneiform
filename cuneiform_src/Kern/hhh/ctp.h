#ifndef	_C_T_P_
#define _C_T_P_

#include "cttypes.h"

#define CTP_FILE_NAME	"cunei$.ctp"

struct ctp_hdr {
	#define SIGNA	"CT Picture"
	Word8 Signatura[10];
	};

struct ctp_pic_hdr {
Word32	pic_size;	// in bytes
Word16	x_off;		// in pixels
Word16	y_off;		// in pixels
Word16	w;		// in pixels
Word16	h;		// in pixels
Word16	resolution;
Word16	bpl;		// bytes per line. Only if native format
Word8    bitpix;
Word8	type;
#define ctp_BW          0
#define ctp_greytone    1
#define ctp_color	2
#define ctp_2xx		10 // compressed
#define ctp_4xx		11 // contured
#define ctp_native      64 /* if type less this const
			      It one of native formats */
#define ctp_tiff        65
#define ctp_gif         66
#define ctp_pcx         67
#define ctp_bmp         68
#define ctp_wmf		69
#define ctp_jpeg	70
Word8    PicName[32];
};

typedef struct ctp_pic_hdr	pic_id;

#define strip_H	16

typedef struct {
	Word16 len; // strip len in bytes
	Word16 col; // begining of strip from strip_header.left(in bytes)
	 } strip;

typedef struct {
	Word16 top; // adjusted to strip_H bound
	Word16 left;
	Word16 n_strip;
	Word16 handle;
	 } strip_header;

#endif  // _C_T_P_
